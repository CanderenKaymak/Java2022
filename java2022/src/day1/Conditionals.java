package day1;

public class Conditionals {
    public static void main(String[] args) {
        int number = 14;

        if (number < 20) {
            System.out.println("Sayi 20'den kucuktur.");
        }
        else if (number == 20){
            System.out.println("Sayi 20'ye esittir.");
        }
        else {
            System.out.println("Sayi 20'den buyuktur.");
        }
    }
}
